    private void jButtoninsertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtoninsertActionPerformed
        String tot = jTextFieldvalTotalPedido.getText();
        tot = tot.replaceAll("R$", "");
        tot = tot.replaceAll(",", "");
        double d = Double.parseDouble(tot);
        if(jFormattedTextFieldcpfcliente.getText().equals("") || jFormattedTextFieldcpfvendedor.getText().equals("") || jTextFieldqtditenspedidos.getText().equals("0.0") 
            || jFormattedTextFielddatapedido.getText().equals("") || jTextFieldqtditenspedidos.getText().equals("") || jTextFieldvalTotalPedido.getText().equals("0.0") || jTextFieldvalTotalPedido.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Para fazer um pedido todos os dados devem ser preenchidos e a tabela nao pode ser vazia.");
            return;
        }
        if(limite < d){
            JOptionPane.showMessageDialog(null, "Valor de compra acima do limite do cliente");
        }else{
            cadastroPedido.add(pedido);
            limite = limite - Double.parseDouble(jTextFieldvalTotalPedido.getText());
            ((Cliente)cadastro.get(posFuncPessoaC)).setLimiteDisponivel(limite);
            alteraComponentes();
            i = 0;
        }    
    }//GEN-LAST:event_jButtoninsertActionPerformed

    private void jButtondeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtondeleteActionPerformed
        if(posFuncPedido >= 0){
            cadastroPedido.get(posFuncPedido).getCliente().setLimiteDisponivel(Double.parseDouble(jTextFieldvalTotalPedido.getText()) + cadastroPedido.get(posFuncPedido).getCliente().getLimiteDisponivel());
            cadastroPedido.get(posFuncPedido).getCliente().removePedido(cadastroPedido.get(posFuncPedido));
            cadastroPedido.get(posFuncPedido).getVendedor().removePedido(cadastroPedido.get(posFuncPedido));
            for(int y = 0; y < cadastroPedido.get(posFuncPedido).getItens_pedidos().size();y++){
                    cadastroPedido.get(posFuncPedido).getItens_pedidos().get(y).getProduto().setQtdeEstoque(
                    cadastroPedido.get(posFuncPedido).getItens_pedidos().get(y).getQtdeVendida() + cadastroPedido.get(posFuncPedido).getItens_pedidos().get(y).getProduto().getQtdeEstoque());
                    cadastroPedido.get(posFuncPedido).removeItem(cadastroPedido.get(posFuncPedido).getItens_pedidos().get(y));
            }
            cadastroPedido.remove(posFuncPedido);
        }
        alteraComponentes();
    }//GEN-LAST:event_jButtondeleteActionPerformed

