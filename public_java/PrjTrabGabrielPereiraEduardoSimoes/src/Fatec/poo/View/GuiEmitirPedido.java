    private String[] listarProdutos(){
        String[] listaProd = new String[cadastroProd.size()];
        
        for (int x=0; x < cadastroProd.size(); x++){
            listaProd[x] = cadastroProd.get(x).getCodigo();
        
       }
        return listaProd;
    }
    
