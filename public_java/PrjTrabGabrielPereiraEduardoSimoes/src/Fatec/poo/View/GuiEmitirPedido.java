    private void jButtonremoveritemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonremoveritemActionPerformed
        DefaultTableModel modelo = (DefaultTableModel) jTablepedido.getModel();
        if (jTablepedido.getSelectedRow() >= 0){
            int x = jTablepedido.getSelectedRow();
            Object cod = jTablepedido.getValueAt(x, 0);
            Object qtd = jTablepedido.getValueAt(x, 3);
            modelo.removeRow(jTablepedido.getSelectedRow());
            jTablepedido.setModel(modelo);
            String c = String.valueOf(cod);
            String q = String.valueOf(qtd);
            Double qtde = Double.parseDouble(q);
            devolveItens(c, qtde);
        }
        else{
            JOptionPane.showMessageDialog(null, "Favor selecionar uma linha");
        }
        i--;
        calcularTotalitens();
        calcularTotalsubtotal();
    }//GEN-LAST:event_jButtonremoveritemActionPerformed

    private void jFormattedTextFielddatapedidoFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_jFormattedTextFielddatapedidoFocusLost
        boolean dataValida,pagamento;
            dataValida = validarData(jFormattedTextFielddatapedido.getText());
            if(dataValida == false){
                JOptionPane.showMessageDialog(null, "Data Inválida.");
            }
            else{
                jComboBoxformaspagamento.setEnabled(true);
                pedido = new Pedido(jTextFieldnumPedido.getText(),jFormattedTextFielddatapedido.getText());

                int p = jComboBoxformaspagamento.getSelectedIndex();
                if(p == 0){
                    pagamento = false;
                }
                else{
                    pagamento = true;
                }
                pedido.setFormaPagamento(pagamento);
                jFormattedTextFieldcpfcliente.setEnabled(true);
                jButtonverificacliente.setEnabled(true);
            }      
    }//GEN-LAST:event_jFormattedTextFielddatapedidoFocusLost

    private void jTextFieldqtdvendidaFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_jTextFieldqtdvendidaFocusLost
        int qtde = Integer.parseInt(jTextFieldqtdvendida.getText());
                if(qtde <= 0){
                    JOptionPane.showMessageDialog(null, "Quantidade nao pode ser menor ou igual a zero");
                    jButtoninseriritem.setEnabled(false);
                } 
                else if(qtde > cadastroProd.get(posFuncProd).getQtdeEstoque()){
                    jButtoninseriritem.setEnabled(false);
                    JOptionPane.showMessageDialog(null, "Quantidade solicitada acima do que temos no estoque,quantidade deste produto no estoque: " + cadastroProd.get(posFuncProd).getQtdeEstoque());
                }
                
                else if(qtde > 0 && qtde <= cadastroProd.get(posFuncProd).getQtdeEstoque() ){
                    if(posFuncProd >= 0){
                        ItemPedido item = new ItemPedido((i + 1), qtde, cadastroProd.get(posFuncProd));
                        jButtoninseriritem.setEnabled(true);
                        pedido.addItem(item);
                        jTextFieldnomeproduto.setText(cadastroProd.get(posFuncProd).getDescricao());
                    }
            }
    }//GEN-LAST:event_jTextFieldqtdvendidaFocusLost

    public void devolveItens(String cod,double qtd){
        for(int y = 0;y < cadastroProd.size();y++){
            if(cadastroProd.get(y).getCodigo().equals(cod)){
                cadastroProd.get(y).setQtdeEstoque(cadastroProd.get(y).getQtdeEstoque() + qtd);
            }
        }
        
    }
    
    
    public boolean validarData(String data){
        try {
            
            SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");
            
            sdf.setLenient(false);
           
            sdf.parse(data);
            
            return true;
        } catch (ParseException ex) {
            return false;
        }
    }
    
    
   


    
    
    
    
    
    
    public void calcularTotalsubtotal(){
    //DecimalFormat df = new DecimalFormat("#,##0.00");
    double somaTotal=0;int x;
    for(x=0; x<jTablepedido.getRowCount();x++){
        Object obj = jTablepedido.getValueAt(x, 4);
        String s = String.valueOf(obj);
        s = s.replaceAll("\\.", "");
        somaTotal += Double.parseDouble(s);
    }
    String tot = String.valueOf(somaTotal/10);
    jTextFieldvalTotalPedido.setText(tot);
    
    }
