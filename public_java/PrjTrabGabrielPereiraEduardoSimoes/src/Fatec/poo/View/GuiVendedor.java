    private void jButtonalterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonalterarActionPerformed
        jTextFieldcpf.setEnabled(false);
        
        String ddd = (String)jComboBoxddds.getSelectedItem();
        String uf = (String) jComboBoxestados.getSelectedItem();
        if(jTextFieldnome.getText().equals("") || jTextFieldcidade.getText().equals("") || jTextFieldendereco.getText().equals("") || 
                jTextFieldsalbase.getText().equals("") || jTextFieldtxcomissao.getText().equals("") || jFormattedTextFieldCEP1.getText().equals("     -   ")
                || jFormattedTextFieldtelefone.getText().equals("    -    ") || uf.equalsIgnoreCase("uf") || ddd.equals("DDD")){
            JOptionPane.showMessageDialog(null, "Todos os campos devem ser preenchidos e um estado e um ddd devem ser selecionados");
        }
        else{
        cadastro.get(posPessoa).setNome(StringUtils.capitalize(jTextFieldnome.getText()));
        ((Vendedor)cadastro.get(posPessoa)).setSalBase(Double.parseDouble(jTextFieldsalbase.getText().replace(',', '.')));
        cadastro.get(posPessoa).setCep(jFormattedTextFieldCEP1.getText());
        cadastro.get(posPessoa).setCidade(StringUtils.capitalize(jTextFieldcidade.getText()));
        cadastro.get(posPessoa).setEndereco(StringUtils.capitalize(jTextFieldendereco.getText()));
        cadastro.get(posPessoa).setTelefone(jFormattedTextFieldtelefone.getText());
        ((Vendedor)cadastro.get(posPessoa)).setTaxaComissao(Double.parseDouble(jTextFieldtxcomissao.getText().replace(',', '.')));
        cadastro.get(posPessoa).setUf(String.valueOf(jComboBoxestados.getSelectedItem()));
        cadastro.get(posPessoa).setDdd(String.valueOf(jComboBoxddds.getSelectedItem()));
        alteracomponentes();
        }
    }//GEN-LAST:event_jButtonalterarActionPerformed

    private void jButtondeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtondeleteActionPerformed
        if(posPessoa >= 0){
            cadastro.remove(posPessoa);
            posPessoa--;
        }
        alteracomponentes();
    }//GEN-LAST:event_jButtondeleteActionPerformed

