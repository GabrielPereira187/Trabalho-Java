    private void jButtonconsultaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonconsultaActionPerformed
        boolean valido = Pessoa.verificaCPF(jTextFieldcpf.getText());
        if(valido == false){
            JOptionPane.showMessageDialog(null, "CPF inválido");
            jTextFieldcpf.setText(null);
        }
        else{
                int x;
                for(x = 0; x < cadastro.size();x++){
                    if(cadastro.get(x).getCpf().equals(jTextFieldcpf.getText()) && cadastro.get(x) instanceof Vendedor){
                        break;
                    }
                }
                if (x < cadastro.size()){
                    posPessoa = x; //localizou o objeto Funcionario (horista, mensalista ou comissionado) no ArrayList
                }else{
                    posPessoa = -1;//não localizou o objeto Projeto no ArrayList
                }

                if(posPessoa >= 0){
                    jTextFieldnome.setEnabled(false);
                    jButtonalterar.setEnabled(true);
                    jButtondelete.setEnabled(true);
                    jButtoninsert.setEnabled(false);
                    jButtonconsulta.setEnabled(false);
                    jTextFieldnome.setText(cadastro.get(posPessoa).getNome());
                    jTextFieldendereco.setText(cadastro.get(posPessoa).getEndereco());
                    jTextFieldcidade.setText(cadastro.get(posPessoa).getCidade());
                    jFormattedTextFieldCEP1.setText(cadastro.get(posPessoa).getCep());
                    jFormattedTextFieldtelefone.setText(cadastro.get(posPessoa).getTelefone());
                    jTextFieldtxcomissao.setText(String.valueOf(((Vendedor)cadastro.get(posPessoa)).getTaxaComissao()));
                    jTextFieldsalbase.setText(String.valueOf(((Vendedor)cadastro.get(posPessoa)).getSalBase()));
                    jComboBoxestados.getModel().setSelectedItem(cadastro.get(posPessoa).getUf());
                    jComboBoxddds.getModel().setSelectedItem(cadastro.get(posPessoa).getDdd());
                }
                else{
                    jButtonalterar.setEnabled(false);
                    jButtondelete.setEnabled(false);
                    jButtoninsert.setEnabled(true);
                    jButtonconsulta.setEnabled(false);
                    jTextFieldnome.requestFocus();
                }
                jTextFieldnome.setEnabled(true);
                jTextFieldsalbase.setEnabled(true);
                jTextFieldcpf.setEnabled(false);
                jTextFieldcidade.setEnabled(true);
                jTextFieldendereco.setEnabled(true);
                jFormattedTextFieldCEP1.setEnabled(true);
                jFormattedTextFieldtelefone.setEnabled(true);
                jTextFieldtxcomissao.setEnabled(true);
                jComboBoxestados.setEnabled(true);
                jComboBoxddds.setEnabled(true);
        }
    }//GEN-LAST:event_jButtonconsultaActionPerformed

    private void jButtoninsertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtoninsertActionPerformed
        String ddd = (String)jComboBoxddds.getSelectedItem();
        String uf = (String) jComboBoxestados.getSelectedItem();
        if(jTextFieldnome.getText().equals("") || jTextFieldcidade.getText().equals("") || jTextFieldendereco.getText().equals("") || 
                jTextFieldsalbase.getText().equals("") || jTextFieldtxcomissao.getText().equals("") || jFormattedTextFieldCEP1.getText().equals("     -   ")
                || jFormattedTextFieldtelefone.getText().equals("    -    ") || uf.equalsIgnoreCase("uf") || ddd.equals("DDD")){
            JOptionPane.showMessageDialog(null, "Todos os campos devem ser preenchidos e um estado e um ddd devem ser selecionados");
        }
        else{
        Pessoa pessoa = null;
        Double salBase = Double.parseDouble(jTextFieldsalbase.getText().replace(',', '.'));
        
        pessoa = new Vendedor(jTextFieldcpf.getText(),StringUtils.capitalize(jTextFieldnome.getText()),salBase);
        Double tx = Double.parseDouble(jTextFieldtxcomissao.getText().replace(',', '.'));
        ((Vendedor)pessoa).setTaxaComissao(tx);
        pessoa.setUf(String.valueOf(jComboBoxestados.getSelectedItem()));
        pessoa.setCep(jFormattedTextFieldCEP1.getText());
        pessoa.setTelefone(jFormattedTextFieldtelefone.getText());
        pessoa.setCidade(StringUtils.capitalize(jTextFieldcidade.getText()));
        pessoa.setDdd(String.valueOf(jComboBoxddds.getSelectedItem()));
        pessoa.setEndereco(StringUtils.capitalize(jTextFieldendereco.getText()));
        cadastro.add(pessoa);
        alteracomponentes();
        }
        
        
    }//GEN-LAST:event_jButtoninsertActionPerformed
    
    
